// ternary operator (used to reduce if and else lines to single line)
//syntax
// condition?true expression : false expression

var num=10;

console.log(num%2==0?"even":"odd");  //even

console.log(num==0?"zero":"non zero");  // num=0

console.log(num%5==0?"/ by 5":"not / by 5");  // divisible by 5


