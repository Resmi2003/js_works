// Function with parameter and not return value

// functionName greeting
function greetings(name){

    console.log(`Good morning ${name}`);
    
}

greetings("Resmi");
greetings("Kavya");



// functionName addNumber
function addNumber(num1,num2){

    console.log(`Addition result=${num1+num2}`);
    
}

addNumber(100,200);
addNumber(300,500);



// subtract(100,20)
function subtractNumber(num1,num2){

    console.log(`Subtraction results=${num1-num2}`);
    
}

subtractNumber(100,20);




// division(100,5)
function divisionNumber(num1,num2){

    console.log(`Division results=${num1/num2}`);
    
}

divisionNumber(100,5);



// multiplication(10,20)
function multiplicationNumber(num1,num2){

    console.log(`Multiplication results=${num1*num2}`);
    
}

multiplicationNumber(10,20);





